package bean;

public class Star {
	private int starId;
	
	private String starName;
	
	private String starProfile;

	private String birth;

	private String constellation;

	private String bloodType;

	private String height;

	private String weight;

	private String job;

	private String masterWork;

	private boolean followed;

	public int getStarId() {
		return starId;
	}

	public void setStarId(int starId) {
		this.starId = starId;
	}

	public String getStarName() {
		return starName;
	}

	public void setStarName(String starName) {
		this.starName = starName;
	}

	public String getStarProfile() {
		return starProfile;
	}

	public void setStarProfile(String starProfile) {
		this.starProfile = starProfile;
	}

	public String getBirth() {
		return birth;
	}

	public void setBirth(String birth) {
		this.birth = birth;
	}

	public String getConstellation() {
		return constellation;
	}

	public void setConstellation(String constellation) {
		this.constellation = constellation;
	}

	public String getBloodType() {
		return bloodType;
	}

	public void setBloodType(String bloodType) {
		this.bloodType = bloodType;
	}

	public String getHeight() {
		return height;
	}

	public void setHeight(String height) {
		this.height = height;
	}

	public String getWeight() {
		return weight;
	}

	public void setWeight(String weight) {
		this.weight = weight;
	}

	public String getJob() {
		return job;
	}

	public void setJob(String job) {
		this.job = job;
	}

	public String getMasterWork() {
		return masterWork;
	}

	public void setMasterWork(String masterWork) {
		this.masterWork = masterWork;
	}

	public boolean isFollowed() {
		return followed;
	}

	public void setFollowed(boolean followed) {
		this.followed = followed;
	}
}
